"""Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y
división"""
numero_1 = float(input("Ingrese un número:"))
numero_2 = float(input("Ingrese otro número:"))

suma = numero_1+numero_2
resta = numero_1-numero_2
multiplicacion = numero_1*numero_2
division = numero_1/numero_2
print(f"La suma es {suma}\nLa resta es {resta}\nLa multiplicacion es {multiplicacion}\nLa division es {division}")